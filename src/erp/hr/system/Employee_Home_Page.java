private void job_description(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_job_description
// TODO add your handling code here:
    ERPHRSYSTEM obj=new ERPHRSYSTEM();
    String name="";
    String query="select description from position_id where position_id=(Select position_id from Employee where username='"+obj.username+"')";
    try
    {
        obj.rs=obj.stm.executeQuery(query);
        obj.rs.next();
        name=obj.rs.getString(1);
    }
    catch(Exception e)
    {
        System.out.println("Error: "+e);
    }
    JOptionPane.showMessageDialog(null, "Job Description: "+name);
}//GEN-LAST:event_job_description

