private void view_supervisor(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_view_supervisor
// TODO add your handling code here:
    ERPHRSYSTEM obj=new ERPHRSYSTEM();
    String name="";
    String query="select first_name, middle_name, last_name from Employee where employee_id=(select reporting_authority from Employee where username='"+obj.username+"')";
    try
    {
        obj.rs=obj.stm.executeQuery(query);
        obj.rs.next();
        name=obj.rs.getString(1).toString()+" "+obj.rs.getString(2).toString()+" "+obj.rs.getString(3).toString();
    }
    catch(Exception e)
    {
        System.out.println("Error: "+e);
    }
    JOptionPane.showMessageDialog(null, "The supervisor name is: "+name);
}//GEN-LAST:event_view_supervisor

